        $user = Auth::user();
        if($user->userType == 1){
            $event_id = $request->event_id;
            $post = new post();
            $post->body = $request->body;
            $post->institute_id = $user->id;
            $post->event_id = $event_id;
            $post->save();
            return redirect()->route('event',$event_id);
        }
        return route('home');
    }

